#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list *new_lst;
	t_list *new_member;

	if (!f || !del)
		return (NULL);
	new_lst = NULL;
	while (lst)
	{
		if (!(new_member = ft_lstnew(f(lst->content))))
		{
			ft_lstclear(&new_lst, del);
			return (NULL);
		}
		ft_lstadd_back(&new_lst, new_member);
		lst = lst->next;
	}
	return (new_lst);
}
